#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	*str;

	str = av[1];
	i = 0;
	if (ac == 2)
	{
		while (str[i])
		{
			if (((str[i] <= 'M') && (str[i] >= 'A'))
				|| ((str[i] <= 'm') && (str[i] >= 'a')))
				str[i] += 13;
			else if (((str[i] >= 'M') && (str[i] <= 'Z'))
				|| ((str[i] >= 'm') && (str[i] <= 'z')))
				str[i] -= 13;
			write(1, &str[i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
